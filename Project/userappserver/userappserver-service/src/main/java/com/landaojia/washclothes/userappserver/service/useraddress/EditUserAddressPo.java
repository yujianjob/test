package com.landaojia.washclothes.userappserver.service.useraddress;

/**
 * 编辑用户地址，参数
 * @author liuxi
 */
public class EditUserAddressPo {
	/**
	 * id
	 */
	private Long id;
	
	/**
	 * 用户id
	 */
	private String userId;
	
	/**
	 * 收发货人或联系人
	 */
	private String consigneeName;
	
	/**
	 * 手机
	 */
	private String consigneeMpNo;
	
	/**
	 * 地址
	 */
	private String address;
	
	/**
	 * 是否默认
	 */
	private boolean defaultFlag;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getConsigneeName() {
		return consigneeName;
	}

	public void setConsigneeName(String consigneeName) {
		this.consigneeName = consigneeName;
	}

	public String getConsigneeMpNo() {
		return consigneeMpNo;
	}

	public void setConsigneeMpNo(String consigneeMpNo) {
		this.consigneeMpNo = consigneeMpNo;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public boolean getDefaultFlag() {
		return defaultFlag;
	}

	public void setDefaultFlag(boolean defaultFlag) {
		this.defaultFlag = defaultFlag;
	}
}
