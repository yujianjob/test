package com.landaojia.washclothes.userappserver.service.order;

import java.math.BigDecimal;

/**
 * 订单产品小计对象(同产品累计了数量和金额)
 * @author liuxi
 */
public class WashItemSubtVo {
	
	/**
	 * 洗涤条目名称
	 */
	private String washItemName;
	
	/**
	 * 单价
	 */
	private BigDecimal price;
	
	/**
	 * 小计价格
	 */
	private BigDecimal subtPrice;
	
	/**
	 * 数量
	 */
	private Integer subtNum;

	public String getWashItemName() {
		return washItemName;
	}

	public void setWashItemName(String washItemName) {
		this.washItemName = washItemName;
	}

	public BigDecimal getSubtPrice() {
		return subtPrice;
	}

	public void setSubtPrice(BigDecimal subtPrice) {
		this.subtPrice = subtPrice;
	}

	public Integer getSubtNum() {
		return subtNum;
	}

	public void setSubtNum(Integer subtNum) {
		this.subtNum = subtNum;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}
}
