$(document).ready(function () {

    //选择人员所属站点
    $("#nodeidSelectByManager").click(function () {
        var nodetype = $("#ManagerInfo_Type").val();
        var url = $("#hdNodeSelectByManagerUrl").val();
        $(this).attr("href", url + "?nodetype=" + nodetype + "&source=1");
    })



    //选择上级人员
    $("#opidSelectByManager").click(function () {
        var url = $("#hdOPIDSelectByManagerUrl").val();
        $(this).attr("href", url + "?source=3");
    })
});
